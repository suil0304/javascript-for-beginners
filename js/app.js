const caculator = {
    /**
     * 둘을 더해보는 어드밴쳐 타임
     * @param {number} a 
     * @param {number} b 
     * @returns {number}
     */
    add: function(a, b) {
        return a + b;
    },
    /**
     * 둘을 빼보는 어드밴쳐 타임
     * @param {number} a 
     * @param {number} b 
     * @returns {number}
     */
    sub: function(a, b) {
        return a - b;
    },
    /**
     * 둘을 곱해보는 어드밴쳐 타임
     * @param {number} a 
     * @param {number} b 
     * @returns {number}
     */
    mult: function(a, b) {
        return a * b;
    },
    /**
     * 둘을 나눠보는 어드밴쳐 타임
     * @param {number} a 
     * @param {number} b 
     * @returns {number}
     */
    div: function(a, b) {
        return a / b;
    }
};